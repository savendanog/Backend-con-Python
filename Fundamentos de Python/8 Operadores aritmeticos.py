#Operaciones con numeros:
print(10 +10) #Suma
print(10 -5) #Resta
print(4 *5) #Multiplicación
print(10/2) #División: devuelve siempre un numero decimal
print (10 // 3) #División sólo con el resultado entero
print(10 % 3) #Modulo: el residuo de una división, en este caso es 1
print(2**3) #Potenciacion: 2 elevado a la 3
#Python resuelve las operaciones siguiendo el orden en que se deben resolver:
#parentesis, exponentes, multiplicaciones, divisiones, adiccion y sustracion


#Operaciones con strings:
print("Hola" + " mundo") #concatena o une las cadenas de texto (Hola mundo)
print("Hola" * 3) #Imprime en 3 veces la cadena de texto (HolaHolaHola)

